
public class Target 
{
	int X;
	int Y;
	int height;
	boolean hit;
	
	public Target()
	{
		
	}
	
	public void init()
	{
		
	}

	public int getX() {
		return X;
	}

	public void setX(int x) {
		X = x;
	}

	public int getY() {
		return Y;
	}

	public void setY(int y) {
		Y = y;
	}

	public boolean isHit() {
		return hit;
	}

	public void setHit(boolean hit) {
		this.hit = hit;
	}
}
